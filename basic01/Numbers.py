# Integer
a = 10           # an integer
print("Integer a =", a, "| Type:", type(a))

# Float
b = 3.14         # a float number
print("Float b =", b, "| Type:", type(b))

# Complex number
c = 2 + 3j       # a complex number
print("Complex c =", c, "| Type:", type(c))
print("Real part of c:", c.real)
print("Imaginary part of c:", c.imag)

# Arithmetic operations
x = 15
y = 4

print("\nArithmetic Operations between x =", x, "and y =", y)
print("Addition:", x + y)
print("Subtraction:", x - y)
print("Multiplication:", x * y)
print("Division:", x / y)
print("Floor Division:", x // y)
print("Modulus:", x % y)
print("Exponentiation (x^y):", x ** y)
