
# Creating a variable and assigning an integer value
x = 10           # x holds the value 10

# Creating a variable and assigning a string value
name = "Varsha Mewada"   # name holds the text "Alice"

# Creating a float variable
pi = 3.14        # pi holds a decimal number

# Boolean variable (True or False)
is_active = True # is_active holds a boolean value

# Reassigning value to a variable
x = 20           # x now holds the new value 20

# Using variables in expressions
total = x + 5    # total will be 25 (20 + 5)

# Printing variables
print(name)      # Output: Alice
print(total)     # Output: 25

# Variables can hold any type of data without declaring the type
data = [1, 2, 3] # data now holds a list
