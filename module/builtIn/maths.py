# Importing the math module
import math

# 1. math.sqrt(x): Returns the square root of x
number = 16
square_root = math.sqrt(number)
print(f"The square root of {number} is: {square_root}")

# 2. math.pow(x, y): Returns x raised to the power y
base = 3
exponent = 4
power_result = math.pow(base, exponent)
print(f"{base} raised to the power {exponent} is: {power_result}")

# 3. math.pi: Returns the value of π
pi_value = math.pi
print(f"The value of π (pi) is: {pi_value}")

# 4. math.factorial(x): Returns the factorial of x
fact_number = 5
factorial_result = math.factorial(fact_number)
print(f"The factorial of {fact_number} is: {factorial_result}")
